# Este script permite introducir dos números y como resultado regresa la suma de ambos

# Pedir al usuario que introduzca el primer número
firstnumber = int(input("Introduzca el primer número: "))

print("EL NUMERO INTRODUCIDO ES --->:", firstnumber)

# Pedir al usuario que introduzca el segundo número
secondnumber = int(input("Introduzca el segundo número: "))

print("EL NUMERO INTRODUCIDO ES --->:", secondnumber)

# Calcular la suma de los dos números
result = firstnumber + secondnumber

# Mostrar el resultado de la suma
print("LA SUMATORIA RESULTANTE ES --->:", result)