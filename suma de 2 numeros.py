# Este script permite introducir dos números y como resultado regresa la suma de ambos

# Pedir al usuario que introduzca el primer número
primer_numero = int(input("Introduzca el primer número: "))

# Pedir al usuario que introduzca el segundo número
segundo_numero = int(input("Introduzca el segundo número: "))

# Calcular la suma de los dos números
suma = primer_numero + segundo_numero

# Mostrar el resultado de la suma
print("La suma de los dos números es:", suma)